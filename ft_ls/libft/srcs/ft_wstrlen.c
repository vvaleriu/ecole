#include <libft.h>

size_t		ft_wstrlen(const wchar_t *s)
{
	int		i;

	i = 0;
	while (*s)
	{
		if (*s < 0x80)
			i++;
		else if (*s < 0x800)
			i += 2;
		else if (*s < 0x10000)
			i += 3;
		else if (*s < 0x200000)
			i += 4;
		s++;
	}
	return (i);
}
